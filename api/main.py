def _safety_identifier(user_id: str) -> str:
    salt = os.environ.get("SAFETY_ID_SALT", "y-local-demo")
    return "y_" + hashlib.sha256(f"{salt}|{user_id}".encode("utf-8")).hexdigest()[:24]


def _deterministic_checkpoint(
    state: dict,
    conversation_id: str,
    preferred_concepts: list[str] | None = None,
) -> dict:
    """Build a useful local checkpoint without another full LLM decode.

    The lesson has already been generated by the teacher. On the local edge
    path a second Gemma vision/text turn made the toolbar stay busy long after
    the answer was visible. The checkpoint remains personalized by the newly
    extracted concepts and learner uncertainty, while cloud teachers may still
    use their structured checkpoint generator.
    """
    concepts = [str(item) for item in (preferred_concepts or []) if item][:2]
    if not concepts:
        concepts = [
            str(item.get("name"))
            for item in state.get("concept_beliefs", [])[:2]
            if isinstance(item, dict) and item.get("name")
        ]
    label = concepts[0].replace("-", " ") if concepts else "the lesson's main idea"
    return {
        "conversation_id": conversation_id,
        "question": f"Solve a fresh example using {label}, then explain why your method works.",
        "concepts": concepts or ["lesson transfer"],
        "rubric": "Check the method, result, and whether the explanation is independent.",
        "difficulty": "introductory",
    }


def get_teacher(model_choice: str = DEFAULT_MODEL_CHOICE) -> Teacher:
    """Return the cached Teacher matching the toolbar's `model_choice`.

    Defaults to the edge baseline so /health and warmup never need to know
    about the user's selection.
    """
    return resolve_teacher(model_choice)


